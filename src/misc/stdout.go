func ClearStdout() {
	fmt.Print("\033[2J")
	fmt.Print("\033[H")
	
}

func ForceClearStdout() error {
	var clearCommand *exec.Cmd

	switch runtime.GOOS{
	case "windows":
		clearCommand = exec.Command("cls")

	default:
		clearCommand = exec.Command("clear")

	}

	clearCommand.Stdout = os.Stdout
	clearCommand.Stderr = os.Stderr

	return clearCommand.Run()

}

